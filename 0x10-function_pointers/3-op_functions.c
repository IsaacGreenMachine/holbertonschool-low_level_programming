#include "3-calc.h"
/**
 * op_add - does operation
 * @a: number a
 * @b: number b
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - does operation
 * @a: number a
 * @b: number b
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - does operation
 * @a: number a
 * @b: number b
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - does operation
 * @a: number a
 * @b: number b
 * Return: quotient
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - does operation
 * @a: number a
 * @b: number b
 * Return: modulo
 */
int op_mod(int a, int b)
{
return (a % b);
}
